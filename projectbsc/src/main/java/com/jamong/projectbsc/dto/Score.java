package com.jamong.projectbsc.dto;

public class Score {
	private String id;
	private int score;
	private String playedDate;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public int getScore() {
		return score;
	}

	public void setScore(int score) {
		this.score = score;
	}

	public String getPlayedDate() {
		return playedDate;
	}

	public void setPlayedDate(String playedDate) {
		this.playedDate = playedDate;
	}

}
